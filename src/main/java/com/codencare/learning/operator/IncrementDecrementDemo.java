package com.codencare.learning.operator;

/**
 *
 * @author iman
 */
public class IncrementDecrementDemo {

    public static void main(String[] args) {
        int z = 10;
        System.out.println("Z++ = " + z++);
        System.out.println("++Z = " + ++z);
        System.out.println("--Z = " + --z);
        System.out.println("Z-- = " + z--);
        System.out.println("Z = " + z);
        float x = 10;
        System.out.println("x++ = " + x++);
        System.out.println("++x = " + ++x);
        System.out.println("--x = " + --x);
        System.out.println("x-- = " + x--);
        System.out.println("x = " + x);
    }
}
